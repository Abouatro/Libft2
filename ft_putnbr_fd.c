#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*str;

	str = "0123456789";
	if (fd == -1)
		return ;
	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		n = n * -1;
		ft_putchar_fd('-', fd);
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	write(fd, &str[n % 10], 1);
}
